    private void ConfigurarTblCliente() {
        TableClienteModel model = new TableClienteModel(new ClienteDAO().listaCliente());
        tblCliente.setModel(model);

    }

    private class TableClienteModel extends AbstractTableModel {

        private List<Cliente> lista;

        public TableClienteModel(List<Cliente> lista) {
            this.lista = lista;
        }

        @Override
        public int getRowCount() {
            return lista.size();
        }

        @Override
        public int getColumnCount() {
            return 5;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Cliente cliente = lista.get(rowIndex);
            if (columnIndex == 0) {
                return cliente.getNome();
            } else if (columnIndex == 1) {
                return cliente.getCpf();
            } else if (columnIndex == 2) {
                return cliente.getEndereco();
            } else if (columnIndex == 3) {
                return cliente.getEmail();
            } else if (columnIndex == 4) {
                return cliente.getTelefone();
            }
            return null;
        }

        @Override
        public String getColumnName(int column) {
            if (column == 0) {
                return "Nome";
            } else if (column == 1) {
                return "Cpf";
            } else if (column == 2) {
                return "Endereço";
            } else if (column == 3) {
                return "Email";
            } else if (column == 4) {
                return "Telefone";
            }
            return null;
        }
    }

    private void bloquear() {
        edtNome.setText("");
        edtCpf.setText("");
        edtEmail.setText("");
        edtEndereco.setText("");
        edtTelefone.setText("");
        edtNome.setEnabled(false);
        edtCpf.setEnabled(false);
        edtEndereco.setEnabled(false);
        edtEmail.setEnabled(false);
        edtTelefone.setEnabled(false);
        btnAlterar.setEnabled(false);
        btnApagar.setEnabled(false);
        btnCancelar.setEnabled(false);
        ConfigurarTblCliente();
    }
