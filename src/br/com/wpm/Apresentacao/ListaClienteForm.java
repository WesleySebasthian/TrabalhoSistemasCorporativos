    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        cliente.setNome(edtNome.getText().trim());
        cliente.setCpf(edtCpf.getText().trim());
        cliente.setEndereco(edtEndereco.getText().trim());
        cliente.setEmail(edtEmail.getText().trim());
        cliente.setTelefone(edtTelefone.getText().trim());
        if (cliente.getNome().isEmpty() || (cliente.getCpf().length() == 9) || cliente.getEndereco().isEmpty() || cliente.getEmail().isEmpty()
                || (cliente.getTelefone().length() == 10)) {
            JOptionPane.showMessageDialog(this, "Preencha Todos os Campo!", "Campo Vazio!", JOptionPane.WARNING_MESSAGE);
        } else {
            ClienteBO clienteBO = new ClienteBO();
            int verificacao = JOptionPane.showConfirmDialog(this, "Tem Certeza que Deseja Alterar?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (verificacao == JOptionPane.YES_OPTION) {
                try {
                    clienteBO.editar(cliente);
                    JOptionPane.showMessageDialog(this, "Alteração Realizada com Sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                    bloquear();
                    ConfigurarTblCliente();
                } catch (NomeDuplicado e) {
                    JOptionPane.showMessageDialog(this, "Nome Inválido!", "Erro Dados!", JOptionPane.WARNING_MESSAGE);
                } catch (CpfDuplicado e) {
                    JOptionPane.showMessageDialog(this, "Cpf Inválido!", "Erro Dados!", JOptionPane.WARNING_MESSAGE);
                } catch (EmailDuplicado e) {
                    JOptionPane.showMessageDialog(this, "Email Inválido!", "Erro Dados!", JOptionPane.WARNING_MESSAGE);
                } catch (TelefoneDuplicado e) {
                    JOptionPane.showMessageDialog(this, "Telefone Inválido!", "Erro Dados!", JOptionPane.WARNING_MESSAGE);
                } catch (HeadlessException e) {
                    e.printStackTrace();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            } else {
                bloquear();
            }
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
        try {
            int verificacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja apagar?", "Confirmacação", JOptionPane.YES_NO_OPTION);
            if (verificacao == JOptionPane.YES_OPTION) {
                ClienteDAO clienteDAO = new ClienteDAO();
                clienteDAO.apagar(cliente.getId());
                JOptionPane.showMessageDialog(this, "Remoção Realizada com Sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                bloquear();
            } else {
                bloquear();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Dados Inacecivel!", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnApagarActionPerformed


