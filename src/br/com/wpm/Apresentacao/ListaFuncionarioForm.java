    private void ConfigurarTblFuncionario() {
        TableFuncionarioModel model = new TableFuncionarioModel(new FuncionarioDAO().listaFuncionario());
        tblFuncionario.setModel(model);

    }

    private class TableFuncionarioModel extends AbstractTableModel {

        private List<Funcionario> lista;

        public TableFuncionarioModel(List<Funcionario> lista) {
            this.lista = lista;
        }

        @Override
        public int getRowCount() {
            return lista.size();
        }

        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Funcionario funcionario = lista.get(rowIndex);
            if (columnIndex == 0) {
                return funcionario.getNome();
            } else if (columnIndex == 1) {
                return funcionario.getCpf();
            } else if (columnIndex == 2) {
                return funcionario.getEndereco();
            } else if (columnIndex == 3) {
                return funcionario.getSalario();
            }
            return null;
        }

        @Override
        public String getColumnName(int column) {
            if (column == 0) {
                return "Nome";
            } else if (column == 1) {
                return "Cpf";
            } else if (column == 2) {
                return "Endere√ßo";
            } else if (column == 3) {
                return "Sal√°rio";
            }
            return null;
        }
    }

    private void bloquear() {
        edtNome.setText("");
        edtCpf.setText("");
        edtEndereco.setText("");
        edtSalario.setText("");
        edtNome.setEnabled(false);
        edtCpf.setEnabled(false);
        edtEndereco.setEnabled(false);;
        edtSalario.setEnabled(false);
        btnAlterar.setEnabled(false);
        btnApagar.setEnabled(false);
        btnCancelar.setEnabled(false);
        ConfigurarTblFuncionario();
    }
