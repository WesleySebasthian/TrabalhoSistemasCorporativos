    private void ConfigurarTblVeiculo() {
        TableVeiculoModel model = new TableVeiculoModel(new VeiculoDAO().listaVeiculo());
        tblVeiculo.setModel(model);
    }

    private class TableVeiculoModel extends AbstractTableModel {

        private List<Veiculo> lista;

        public TableVeiculoModel(List<Veiculo> lista) {
            this.lista = lista;
        }

        @Override
        public int getRowCount() {
            return lista.size();
        }

        @Override
        public int getColumnCount() {
            return 8;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Veiculo veiculo = lista.get(rowIndex);
            if (columnIndex == 0) {
                return veiculo.getMarca();
            } else if (columnIndex == 1) {
                return veiculo.getModelo();
            } else if (columnIndex == 2) {
                return veiculo.getAno();
            } else if (columnIndex == 3) {
                return veiculo.getDescricao();
            } else if (columnIndex == 4) {
                return veiculo.getPlaca();
            } else if (columnIndex == 5) {
                return veiculo.getChassi();
            } else if (columnIndex == 6) {
                return veiculo.getQuantidade();
            } else if (columnIndex == 7) {
                return veiculo.getPreco();
            }
            return null;
        }

        @Override
        public String getColumnName(int column) {
            if (column == 0) {
                return "Marca";
            } else if (column == 1) {
                return "Modelo";
            } else if (column == 2) {
                return "Ano";
            } else if (column == 3) {
                return "Descrição";
            } else if (column == 4) {
                return "Placa";
            } else if (column == 5) {
                return "Chassi";
            } else if (column == 6) {
                return "Quantidade";
            } else if (column == 7) {
                return "Preço";
            }
            return null;
        }

    }

