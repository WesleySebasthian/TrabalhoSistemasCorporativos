    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {
            veiculo.setMarca(edtMarca.getText().trim());
            veiculo.setModelo(edtModelo.getText().trim());
            veiculo.setAno(Integer.parseInt(edtAno.getText().trim()));
            veiculo.setDescricao(edtDescricao.getText().trim());
            veiculo.setPlaca(edtPlaca.getText().trim());
            veiculo.setChassi(edtChassi.getText().trim());
            veiculo.setQuantidade(Integer.parseInt(edtQtd.getText().trim()));
            veiculo.setPreco(Double.parseDouble(edtPreco.getText().trim()));
            if (veiculo.getMarca().isEmpty() || veiculo.getPlaca().isEmpty() || veiculo.getDescricao().isEmpty() || veiculo.getPlaca().isEmpty()
                    || veiculo.getChassi().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha Todos os Campos!", "Campo Vazio!", JOptionPane.WARNING_MESSAGE);
            } else {
                int verificacao = JOptionPane.showConfirmDialog(this, "Tem Certeza que Deseja Alterar?", "Confirmação", JOptionPane.YES_NO_OPTION);
                if (verificacao == JOptionPane.YES_OPTION) {
                    VeiculoBO veiculoBO = new VeiculoBO();
                    veiculoBO.editar(veiculo);
                    JOptionPane.showMessageDialog(this, "Alteração Realizada com Sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
                }
                bloquear();
            }
        } catch (NumberFormatException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(this, "Valores Inválidos!", "Campo Inválido!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void tblVeiculoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVeiculoMouseClicked
        int cod = tblVeiculo.getSelectedRow();
        VeiculoDAO veiculoDAO = new VeiculoDAO();
        veiculo = veiculoDAO.listaVeiculo().get(cod);
        edtMarca.setText(veiculo.getMarca());
        edtModelo.setText(veiculo.getModelo());
        edtAno.setText(Integer.toString(veiculo.getAno()));
        edtPlaca.setText(veiculo.getPlaca());
        edtChassi.setText(veiculo.getChassi());
        edtDescricao.setText(veiculo.getDescricao());
        edtQtd.setText(Integer.toString(veiculo.getQuantidade()));
        edtPreco.setText(Double.toString(veiculo.getPreco()));
        edtMarca.setEnabled(true);
        edtModelo.setEnabled(true);
        edtAno.setEnabled(true);
        edtPlaca.setEnabled(true);
        edtChassi.setEnabled(true);
        edtDescricao.setEnabled(true);
        edtQtd.setEnabled(true);
        edtPreco.setEnabled(true);
    }//GEN-LAST:event_tblVeiculoMouseClicked

