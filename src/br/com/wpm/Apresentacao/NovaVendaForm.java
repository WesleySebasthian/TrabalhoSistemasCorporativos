    private void ConfigurarCmbFuncionario() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) this.cmbFuncionario.getModel();
        model.removeAllElements();
        for (Funcionario funcionario : new FuncionarioDAO().listaFuncionario()) {
            model.addElement(funcionario);
        }
    }

    private void ConfigurarCmbCliente() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) this.cmbCliente.getModel();
        model.removeAllElements();
        for (Cliente cliente : new ClienteDAO().listaCliente()) {
            model.addElement(cliente);
        }
    }

    private void ConfigurarCmbVeiculo() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) this.cmbVeiculo.getModel();
        model.removeAllElements();
        for (Veiculo veiculo : new VeiculoDAO().listaVeiculo()) {
            model.addElement(veiculo);
        }
    }
