    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            Double valor;
            Venda venda = new Venda(((Funcionario) cmbFuncionario.getSelectedItem()), ((Cliente) cmbCliente.getSelectedItem()),
                    ((Veiculo) cmbVeiculo.getSelectedItem()));
            VendaBO vendaBO = new VendaBO();
            if (vendaBO.verificarDesconto(venda.getCliente().getId())) {
                Double desconto = Double.parseDouble(JOptionPane.showInputDialog(this, "Qual a porcentagem de Desconto?"));
                Double valorDescontado = ((Veiculo) cmbVeiculo.getSelectedItem()).getPreco() * (desconto / 100);
                valor = ((Veiculo) cmbVeiculo.getSelectedItem()).getPreco() - valorDescontado;
            } else {
                valor = ((Veiculo) cmbVeiculo.getSelectedItem()).getPreco();
            }
            Date date = new Date();
            java.sql.Date dataSql = new java.sql.Date(date.getTime());
            venda.setValor(valor);
            venda.setData(dataSql);
            if (((Veiculo) cmbVeiculo.getSelectedItem()).getQuantidade() > 0) {
                vendaBO.cadastrar(venda);
                JOptionPane.showMessageDialog(this, "Cadastro Realizado com Sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                ConfigurarCmbCliente();
                ConfigurarCmbFuncionario();
                ConfigurarCmbVeiculo();
            } else {
                JOptionPane.showMessageDialog(this, "Estoque Indisponivel!", "Dado Inv치lido!", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Valor Inv치lido!", "Dado Inv치lido!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed


